<?php
//-----------------------------------------------------------------------------------------------//
defined('BASEPATH') or exit('No direct script access allowed');
//-----------------------------------------------------------------------------------------------//
class C_index extends CI_Controller
{
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	public function __construct()
	{
		parent::__construct();

		if ($this->session->userdata('session_mursmedic_status') == "LOGIN") {
			redirect(site_url("menu"));
		}
	}
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	public function index()
	{
		$this->load->view('V_index');
	}
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	public function login()
	{
		if ($_SERVER['REQUEST_METHOD'] != "POST") {
			echo '
				<script>
					alert("UNKNOWN COMMAND");
					window.location.href = "' . site_url('index') . '";
				</script>
			';
			exit();
		}
		date_default_timezone_set('Asia/Jakarta');
		//-----------------------------------------------------------------------------------------------//
		$si_userid = trim($this->input->post('si_userid'));
		$si_password = trim($this->input->post('si_password'));
		//-----------------------------------------------------------------------------------------------//
		$data = $this->M_library_database->DB_GET_DATA_LOGIN($si_userid, $si_password);
		//-----------------------------------------------------------------------------------------------//
		if (empty($data)) {
			echo '
				<script>
					alert("DATA NOT FOUND");
					window.location.href = "' . site_url('index') . '";
				</script>
			';
			exit();
		}
		//-----------------------------------------------------------------------------------------------//
		//INSERT LOG
		$data_log = array(
			'LG_ID' => $this->M_library_module->GENERATOR_REFF(),
			'UR_ID' => $data[0]->UR_ID,
			'LG_DESC' => "LOGIN",
			'LG_DATE' => date('Y-m-d H:i:s')
		);
		$is_log = $this->M_library_database->DB_INSERT_DATA_LOG($data_log);
		//-----------------------------------------------------------------------------------------------//
		$session_data['session_mursmedic_status'] = "LOGIN";
		$session_data['session_mursmedic_id'] = $data[0]->UR_ID;
		$session_data['session_mursmedic_name'] = $data[0]->UR_NAME;
		$session_data['session_mursmedic_phone'] = $data[0]->UR_PHONE;
		$session_data['session_mursmedic_email'] = $data[0]->UR_EMAIL;
		$session_data['session_mursmedic_grant'] = $data[0]->GT_ID;
		//-----------------------------------------------------------------------------------------------//
		//SET VARIABLE SESSION
		$this->session->set_userdata($session_data);
		//-----------------------------------------------------------------------------------------------//
		redirect(site_url('menu'));
	}
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
	//-----------------------------------------------------------------------------------------------//
}
//-----------------------------------------------------------------------------------------------//
